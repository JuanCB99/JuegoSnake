    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed

        //en este evento se escuchan las teclas pulsadas y se les asigna una acci√≥n 
        //ademas se refresca el marcador de puntos
        lb_puntos.setText(String.valueOf(contadorPuntos));

        switch (evt.getExtendedKeyCode()) {
            case KeyEvent.VK_LEFT:
                snake.cambiarDireccion("L");

                break;
            case KeyEvent.VK_RIGHT:
                snake.cambiarDireccion("R");

                break;
            case KeyEvent.VK_UP:
                snake.cambiarDireccion("U");

                break;
            case KeyEvent.VK_DOWN:
                snake.cambiarDireccion("D");

                break;
            default:

                break;
        }

    }//GEN-LAST:event_formKeyPressed

